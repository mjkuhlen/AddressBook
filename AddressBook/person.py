class Person(object):
    def __init__(self, fname, lname, street, city, state, zipcode, day, month, year):
        self.first_name = fname
        self.last_name = lname
        self.street_address = street
        self.city_address = city
        self.state_address = state
        self.zip_address = zipcode
        self.birth_day = day
        self.birth_month = month
        self.birth_year = year





